package Section4.Learn;

public class BuildInFunctions {
    public static void main(String[] args) {
        System.out.println("Hello Java");

        double sine = Math.sin(5.5);
        System.out.println("The sin of 5.5 is:" + sine);

        double power = Math.pow(5, 5);
        System.out.println("2 to the power of 4 is: " + power);

        double random = Math.random()*100;
        System.out.println("The random value id: " + random);
    }
}
